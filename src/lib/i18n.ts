export function useTranslation() {
  const language = usePreferencesStore((s) => s.language);
  const t = (key: string, fallback?: string): string => {
    return translations[language]?.[key] || translations.en[key] || fallback || key;
  };

  /** Translate a pipeline stage value (e.g. 'first_study' → 'Primer Estudio') */
  const tStage = (stage: string): string => t(`stage.${stage}`, stage);

  /** Translate a user role value (e.g. 'team_leader' → 'Líder de Equipo') */
  const tRole = (role: string): string => t(`role.${role}`, role);

  /** Translate a booking type value (e.g. 'unbaptized_contact' → 'Contacto No Bautizado') */
  const tBookingType = (type: string): string => t(`bookingType.${type}`, type);

  return { t, tStage, tRole, tBookingType, language };
}
