#!/usr/bin/env node
/**
 * render-propagation.mjs — GENERATE docs/qa/PROPAGATION.md from the append-only
 * propagation.jsonl + the fingerprint (audit-anti-drift G1/G13: rendered FROM the
 * log, never memory). Catalog-only: LEAK rows are FINDINGS, not failures — the
 * script exits non-zero only on an INTEGRITY fault (missing fingerprint, a
 * START≠END mismatch, or an unparseable cell), mirroring render-run2's gate.
 *
 * On re-run, a cell id may appear more than once (a corrected instrument re-logs);
 * the LAST occurrence wins, and the supersession is noted.
 */
import { readFileSync, existsSync, writeFileSync } from 'node:fs';
import { execSync } from 'node:child_process';

const QA = 'docs/qa';
const LOG = `${QA}/propagation.jsonl`;
const FP = `${QA}/propagation-fingerprint.json`;
const OUT = `${QA}/PROPAGATION.md`;

if (!existsSync(LOG)) { console.error(`no ${LOG}`); process.exit(1); }
const raw = readFileSync(LOG, 'utf8').trim().split('\n').filter(Boolean);
let parseErrors = 0;
const all = raw.map((l) => { try { return JSON.parse(l); } catch { parseErrors++; return null; } }).filter(Boolean);

// last-wins per id (corrected re-runs supersede)
const byId = new Map();
const superseded = [];
for (const c of all) { if (byId.has(c.id)) superseded.push(c.id); byId.set(c.id, c); }
const cells = [...byId.values()];

const z0 = cells.find((c) => c.id === 'Z0');
const data = cells.filter((c) => c.id !== 'Z0');

// fingerprint (START vs END)
const fpStart = existsSync(FP) ? JSON.parse(readFileSync(FP, 'utf8')) : null;
const sh = (c) => { try { return execSync(c, { encoding: 'utf8' }).trim(); } catch { return '?'; } };
const endSha = sh('git rev-parse --short HEAD');
// Integrity gate = the SUT (app code under src/) must be byte-identical start→end.
// Compare the src/ TREE hash, not the full commit SHA — catalog-only deliverable
// commits (docs/qa, e2e/) legitimately move HEAD but must NOT touch src/.
const startSrc = fpStart ? sh(`git rev-parse ${fpStart.gitSha}:src`) : '?';
const endSrc = sh('git rev-parse HEAD:src');
const startEndOk = fpStart && startSrc !== '?' && startSrc === endSrc;

// tallies
const tally = {};
for (const c of data) tally[c.verdict] = (tally[c.verdict] || 0) + 1;
const leaks = data.filter((c) => c.verdict === 'LEAK' || c.verdict === 'OVER');
const byDomain = {};
for (const c of data) (byDomain[c.domain] ||= []).push(c);

// blast-radius rank for leaks (machine-derived: #expected sites; persist/cross-entity bonus from notes)
const blast = (c) => (c.expected_site_count || 0) + (/persist|localStorage|cookie|sidebar/i.test(JSON.stringify(c)) ? 2 : 0) + (/cascade|cross/i.test(JSON.stringify(c)) ? 2 : 0);

// Cataloged-but-NOT-YET-RUN cells (queued in propagation-catalog.md). Enumerated so
// the render can NEVER read as more complete than it is (3AgentScan honesty finding).
const NOT_RUN = [
  { id: 'E5', cell: 'deactivate↔restore user (subtree cascade + scope-visibility)', src: 'catalog cell (c)' },
  { id: 'E6', cell: 'reparent user (parent change → tree node + manageable scope)', src: 'catalog cell (b) reparent half' },
  { id: 'E7', cell: 'blocked-slot create/delete (booking-wizard grey-out)', src: 'catalog cell (f)' },
  { id: 'C4', cell: 'log-study via the 9-step booking wizard (full study cascade)', src: 'Z0 used a bulk-stage-change substitute' },
];
const notRun = NOT_RUN.filter((n) => !byId.has(n.id));
const deferred = data.filter((c) => c.verdict === 'DEFERRED');

const esc = (s) => String(s ?? '').replace(/\|/g, '\\|').replace(/\n/g, ' ');
const md = `# Diamond — Propagation Expectation-Matrix Audit (catalog-only) — GENERATED

> Machine-generated by \`scripts/render-propagation.mjs\` from \`propagation.jsonl\` + the fingerprint.
> Do NOT hand-edit. LEAK rows are FINDINGS (catalog-only — not fixed this run). Observation is at the
> rendered/state SURFACE; mock-state fetch is corroboration only.

## Environment fingerprint
| field | START | END | ok |
|---|---|---|---|
| git SHA | \`${fpStart?.gitSha ?? '?'}\` | \`${endSha}\` | ${startEndOk ? '✅' : '❌ VOID'} |
| branch | \`${fpStart?.gitBranch ?? '?'}\` | | |
| MOCK_DATE | \`${fpStart?.mockDate ?? '?'}\` | | |
| node / playwright | ${fpStart?.node ?? '?'} / ${fpStart?.playwright ?? '?'} | | |
| seed counts | ${fpStart ? Object.entries(fpStart.seedCounts || {}).map(([k, v]) => `${k}:${v}`).join(' · ') : '?'} | | |
| Z0 | ${z0 ? z0.z0?.verdict : 'MISSING'} (${esc(z0?.z0?.method || '')}) | | |

## Summary (${data.length} cells logged)
| verdict | count |
|---|---|
${Object.entries(tally).map(([k, v]) => `| ${k} | ${v} |`).join('\n')}
| **confirmed LEAK/OVER** | **${leaks.length}** |
${superseded.length ? `\n_Superseded (corrected instrument re-run, last-wins): ${[...new Set(superseded)].join(', ')}._` : ''}

## Cell matrix
| id | domain | mutation | actor | expected sites | verdict | classification |
|---|---|---|---|---|---|---|
${data.map((c) => `| ${c.id} | ${c.domain} | ${esc(c.mutation)} | ${esc(c.actor_role)} | ${c.expected_site_count ?? (c.expected_reflections?.length ?? '?')} | ${c.verdict} | ${esc(c.classification ?? '—')} |`).join('\n')}

## LEAK / OVER findings (ranked by blast radius)
${leaks.length === 0 ? '_None this run._' : leaks.sort((a, b) => blast(b) - blast(a)).map((c) => `- **${c.id}** (blast ${blast(c)}, ${esc(c.classification)}) — ${esc(c.mutation)} → leaked at ${esc(JSON.stringify(c.leak_sites))}. Evidence: ${esc(JSON.stringify(c.evidence)).slice(0, 240)}…`).join('\n')}

## Per-domain coverage (cells run / surfaces covered)
${Object.entries(byDomain).map(([d, cs]) => `- **${d}**: ${cs.length} cells — sites: ${[...new Set(cs.flatMap((c) => (c.expected_reflections || []).map((r) => r.site_id)))].join(', ')}`).join('\n')}

## Coverage disclosure (what was NOT run this batch)
This is an INCREMENTAL audit — it does NOT claim full graph-edge coverage. Honestly enumerated:
- **DEFERRED (harness limit, verified-by-source):** ${deferred.length ? deferred.map((c) => c.id).join(', ') : 'none'} — ContactDetailDialog crashes the Playwright renderer; behavior confirmed by source-trace + F1 + Run-2 specs.
- **Cataloged but NOT RUN (queued in \`propagation-catalog.md\`):**
${notRun.length ? notRun.map((n) => `  - **${n.id}** — ${n.cell} (${n.src})`).join('\n') : '  - none'}
- **Known missed graph edges (false-PASS risk until folded in):** see \`propagation-graph-gaps.md\` (A1–A6: dashboard currentlyStudying/stat-dialogs, tree-node live metrics, /admin family).

## Notes
- Batches landed so far: ${[...new Set(data.map((c) => c.domain))].join(', ')}.
- Integrity gate: START==END (src/ tree hash) ${startEndOk ? 'OK' : 'FAILED'}; parse errors ${parseErrors}.
`;

writeFileSync(OUT, md);
console.log(`PROPAGATION.md — ${data.length} cells, ${leaks.length} leaks, Z0 ${z0?.z0?.verdict}, START==END ${startEndOk}`);
if (!fpStart || !startEndOk || parseErrors) {
  console.error(`INTEGRITY: fingerprint=${!!fpStart} startEnd=${startEndOk} parseErrors=${parseErrors}`);
  process.exit(1);
}
